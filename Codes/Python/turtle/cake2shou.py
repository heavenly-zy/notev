from turtle import*
import time
def drawcake(n):
    tracer(5)
    seth(180)
    penup()
    fd(20*n)
    left(90)
    pendown()
    color('tan','bisque')
    begin_fill()
    fd(15*n)
    left(45)
    circle(8*n,25)
    circle(53*n,40)
    circle(8*n,25)
    left(45)
    fd(13*n)
    end_fill()
    seth(15)
    color('saddlebrown','saddlebrown')
    begin_fill()
    circle(3*n,40)
    circle(2*n,35)    #0
    circle(15*n,10)
    circle(7*n,55)
    circle(50*n,40)
    circle(15*n,25)
    circle(5*n,85)
    fd(3*n)
    circle(-7*n,30)
    circle(n,140)
    fd(2*n)
    circle(-n,140)
    fd(1.5*n)
    circle(1.5*n,120)
    fd(2*n)
    circle(-1.5*n,125)
    fd(3*n)
    circle(1.5*n,120)
    circle(n,40)
    fd(5*n)
    circle(-2*n,120)
    circle(2.5*n,110)
    fd(n)
    circle(-2*n,120)
    circle(2.5*n,120)
    circle(-1.5*n,160)
    fd(3*n)
    circle(n,180)
    fd(4*n)
    circle(-n,130)
    circle(n,90)
    end_fill()
    penup()

def drawcandle():
    tracer(4)
    begin_fill()
    seth(-90)
    color('red','red')
    pendown()
    circle(3,180)
    fd(30)
    circle(3,-180)
    fd(30)
    end_fill()
    fd(-30)
    circle(3,90)
    left(90)
    fd(10)
    begin_fill()
    left(90)
    color('yellow','yellow')
    circle(-5,-60)
    circle(-5,120)
    circle(-10,120)
    left(30)
    circle(-7,-60)
    circle(-5,-30)
    circle(10,-60)
    end_fill()
    penup()

def word():
    co=['red','yellow','blue']
    t=Turtle()
    t.penup()
    t.seth(90)
    t.pensize(2)
    t.goto(-100,-1000)
    for i in range(100):
        t.clear()
        t.pencolor(co[i%len(co)])
        t.write('受受生快！',font=("华文行楷", 60, "normal"))
        t.penup()
        t.fd(10)
        t.pendown()
    
if __name__=='__main__':
    tracer(1)
